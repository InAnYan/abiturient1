def process(data: list[dict[str, str]]):
    for item in data:
        speciality = item["speciality"]

        index = speciality.find("/")
        if index != -1:
            item["speciality"] = speciality[:index].strip()
            item["specialization"] = speciality[index + 1 :].strip()
        else:
            index = speciality.find("(")
            if index != -1:
                item["speciality"] = speciality[:index].strip()
                item["specialization"] = speciality[index + 1 : -1].strip()

        speciality = item["speciality"]
        speciality_space = speciality.find(" ")
        item["speciality_code"] = speciality[:speciality_space]
        item["speciality_name"] = speciality[speciality_space + 1 :]

        del item["speciality"]

        if "specialization" in item:
            specialization = item["specialization"]
            specialization_dot = specialization.find(".")
            speciality_space = specialization.find(" ")

            item["specialization_code"] = specialization[
                specialization_dot + 1 : speciality_space
            ]
            item["specialization_name"] = specialization[speciality_space + 1 :]

            del item["specialization"]


